# Float
myValue = 3.14

# Imprime el valor en pantalla
print(myValue)

# type() muestra el tipo de dato de lo que esta dentro del parentesis
print(type(myValue))

# castear/transformar valor numerico a texto
print(str(myValue) + " is of the data type " + str(type(myValue)))

# Formateo de strings en Python
print(f"{myValue} is of data type ${type(myValue)}")
